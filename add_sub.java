public class add_sub{

    public static void main(String args[]) {
        int a=10;
        int b=6;
        System.out.println(sub(a,b));
        
    }
    
    public static int add(int a, int b){
        int c=a+b;
        return c;
    }

    public static int sub(int a, int b){
        int c=a-b;
        return c;
    }
}
